public class LoopsDemo {
       public static void main(String[] args) {
	System.out.println("For loop:");
	for (int i = 0; i<=3; i++) {
		System.out.println("Test Iteration " + i);
	}
		System.out.println("While loop:");
		int count =1;
		while (count <=3) {
		System.out.println("Retry attempt " + count);	
		count++;
		}
	}
}
